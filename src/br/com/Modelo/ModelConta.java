package br.com.Modelo;

/**
 *
 * @author Diogo
 */
public class ModelConta {
    private static String nome;
    private static String senha;
    private static String tipo;
    private static String saldo;
    private static String CPF;
    private static int n_usuario;
    private static int nova_conta;
    private static int busca_usuario;

    /**
     * @return the CPF
     */
  

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the senha
     */
    public String getSenha() {
        return senha;
    }

    /**
     * @param senha the senha to set
     */
    public void setSenha(String senha) {
        this.senha = senha;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the saldo
     */
    public String getSaldo() {
        return saldo;
    }

    /**
     * @param saldo the saldo to set
     */
    public void setSaldo(String saldo) {
        this.saldo = saldo;
    }


    /**
     * @return the busca_usuario
     */
    public int getBusca_usuario() {
        return busca_usuario;
    }

    /**
     * @param busca_usuario the busca_usuario to set
     */
    public void setBusca_usuario(int busca_usuario) {
        this.busca_usuario = busca_usuario;
    }

    /**
     * @return the n_usuario
     */
    public int getN_usuario() {
        return n_usuario;
    }

    /**
     * @param n_usuario the n_usuario to set
     */
    public void setN_usuario(int n_usuario) {
        this.n_usuario = n_usuario;
    }

    /**
     * @return the nova_conta
     */
    public int getNova_conta() {
        return nova_conta;
    }

    /**
     * @param nova_conta the nova_conta to set
     */
    public void setNova_conta(int nova_conta) {
        this.nova_conta = nova_conta;
    }

    /**
     * @return the CPF
     */
    public String getCPF() {
        return CPF;
    }

    /**
     * @param CPF the CPF to set
     */
    public void setCPF(String CPF) {
        this.CPF = CPF;
    }

    /**
     * @return the n_usuario
     */
 
}
